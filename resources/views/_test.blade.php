                    <h2>Qu’est-ce que l’évaluation des risques professionnels ?</h2><br>
                    <ul>
                        <li>L’évaluation des risques professionnels consiste à identifier les risques auxquels sont soumis les salariés d’un établissement, en vue de mettre en place des actions de prévention pertinentes couvrant les dimensions techniques, humaines et organisationnelles. Elle constitue l’étape initiale de toute démarche de prévention en santé et sécurité au travail.</li>
                    </ul>&nbsp;<br><a name="eztoc1_1_1" id="eztoc1_1_1"></a>

                    <h2>Pourquoi évaluer les risques professionnels ?</h2><br>
                    <ul>
                        <li>L’évaluation des risques professionnels relève de la responsabilité de l’employeur, et s’inscrit dans le cadre de son obligation générale d’assurer la sécurité et de protéger la santé des salariés.</li>
                        <li>L’évaluation des risques professionnels figure parmi les principes généraux de prévention énoncé dans le Code du travail . Celle-ci englobe des actions d’identification et de classement des risques et aussi de mise en place d’actions de prévention. Ces actions ne dispensent pas l’entreprise de mettre également en œuvre des mesures correctives immédiates.</li>
                        <li>L’évaluation des risques professionnels constitue l’un des principaux leviers de progrès dans l’entreprise. Elle lui est utile puisqu’elle peut contribuer à améliorer son fonctionnement tout au long de son évolution, en consolidant la maîtrise des risques avérés mais également en pointant l’apparition de risques à effets différés ou de nouveaux risques, en particulier ceux qui sont liés aux nouvelles organisations ou à de nouveaux procédés industriels.</li>
                        <li>La santé et la sécurité des salariés ne doivent donc pas être dissociés du fonctionnement de l’entreprise (choix techniques, organisation du travail, mobilisation des compétences, formation…). La mise en place d’une démarche de prévention contribuera à améliorer la performance de l’entreprise sur le plan humain et économique.</li>
                    </ul>&nbsp;<br><a name="eztoc1_1_1" id="eztoc1_1_1"></a>

                    <h2>Principes de l’évaluation</h2><br>
                    <ul>
                        <li>La démarche d’évaluation des risques s’appuie sur des principes contribuant à sa réussite et sa pérennité:</li>
                        <ul>
                            <li>Afficher sa volonté de réaliser une évaluation des risques.
                                <p>Lors de l’étape de préparation de l’évaluation, le chef d’entreprise s’engage sur les objectifs, les moyens et les modalités d’organisation et de communication.</p>
                            </li>
                        </ul>
